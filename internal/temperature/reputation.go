package temperature

import (
	"net"
	"sync"
)

// Reputation classifies IPs by origin network type.
type Reputation struct {
	once    sync.Once
	cloud   []*net.IPNet
	torNets []*net.IPNet
}

// NewReputation builds the prefix sets from the embedded CIDR lists.
func NewReputation() *Reputation {
	r := &Reputation{}
	r.once.Do(func() {
		r.cloud = parseCIDRs(cloudCIDRs)
		r.torNets = parseCIDRs(torCIDRs)
	})
	return r
}

// Score returns 0–100 indicating how suspicious the IP origin is.
// 0 = likely residential, 80 = known cloud provider, 100 = known Tor exit.
func (r *Reputation) Score(ip string) int {
	parsed := net.ParseIP(ip)
	if parsed == nil {
		return 0
	}
	for _, cidr := range r.torNets {
		if cidr.Contains(parsed) {
			return 100
		}
	}
	for _, cidr := range r.cloud {
		if cidr.Contains(parsed) {
			return 80
		}
	}
	return 0
}

func parseCIDRs(list []string) []*net.IPNet {
	nets := make([]*net.IPNet, 0, len(list))
	for _, s := range list {
		_, cidr, err := net.ParseCIDR(s)
		if err != nil {
			continue
		}
		nets = append(nets, cidr)
	}
	return nets
}

